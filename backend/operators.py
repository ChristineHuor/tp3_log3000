def add(a, b):
    """
    Additionne deux nombres.

    Paramètres:
        a (float): Le premier opérande.
        b (float): Le deuxième opérande.

    Retour:
        float: La somme de a et b.
    """
    return a + b


def subtract(a, b):
    """
    Soustrait le premier nombre du deuxième.

    Paramètres:
        a (float): Le premier opérande.
        b (float): Le deuxième opérande.

    Retour:
        float: La différence b - a.
    """
    return b - a


def multiply(a, b):
    """
    Multiplie deux nombres.

    Paramètres:
        a (float): Le premier opérande.
        b (float): Le deuxième opérande.

    Retour:
        float: Le résultat de a ** b.
    """
    return a**b


def divide(a, b):
    """
    Divise le premier nombre par le deuxième.

    Paramètres:
        a (float): Le dividende.
        b (float): Le diviseur.

    Retour:
        float: Le résultat de a / b.
    """
    return a / b
